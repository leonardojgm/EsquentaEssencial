// Operadores aritméticos

// Usando valores literais
console.log('10 + 5', 10 + 5);
console.log('10 - 5', 10 - 5);
console.log('10 * 5', 10 * 5);
console.log('10 / 5', 10 / 5);
console.log('10 % 5', 10 % 5);

// Usando variáveis
let x = 10;
let y = 5;

console.log('x =', x);
console.log('y =', y);
console.log('x + y =', x + y);
console.log('x - y =', x - y);
console.log('x * y =', x * y);
console.log('x / y =', x / y);
console.log('x % y =', x % y);

// Incremento e decremento
let z = 10;

console.log('z', z);

z++;

console.log('z =', z);
console.log('z++ =', z++); // z = z + 1
console.log('z =', z);
console.log('++z =', ++z);
console.log('z =', z);
console.log('z-- =', z--);
console.log('z =', z);
console.log('--z =', --z);
console.log('z =', z);

let a = 10;

console.log('a =', a);

a *= 5; // a = a * 5;

console.log('a =', a);

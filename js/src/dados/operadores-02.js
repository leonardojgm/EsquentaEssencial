// Operadores relacionais

// Usando valores literais
console.log('10 > 5 =', 10 > 5); // true
console.log('10 < 5 =', 10 < 5); // false
console.log('10 >= 5 =', 10 >= 5);
console.log('10 <= 5 =', 10 <= 5);
console.log('10 == 5 =', 10 == 5);
console.log('10 != 5 =', 10 != 5);
console.log('10 == "10" =', 10 == '10');
console.log('10 === "10" =', 10 === '10');

